function sortArrayDes(arr) {
  for (let i = 0; i < arr.length - 1; i++) {
    for (let j = 0; j < arr.length - 1 - i; j++) {
      if (arr[j] < arr[j + 1]) {
        const temp = arr[j];
        arr[j] = arr[j + 1];
        arr[j + 1] = temp;
      }
    }
  }
  return arr;
}

const inputArray = [5, 2, 8, 1, 9];
const sortedArray = sortArrayDes(inputArray);
console.log(sortedArray);
